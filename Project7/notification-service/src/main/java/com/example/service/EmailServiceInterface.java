package com.example.service;

/**
 * Интерфейс сервиса для отправки электронных писем.
 * Предоставляет методы для отправки различных типов email-уведомлений пользователям.
 *
 * <p>Данный интерфейс определяет контракт для сервисов отправки email-сообщений,
 * включая стандартные шаблонные письма (приветственные, уведомления об удалении аккаунта)
 * и произвольные сообщения с заданной темой и содержимым.</p>
 *
 * @implSpec Реализации должны гарантировать доставку сообщений или обработку ошибок отправки
 */
public interface EmailServiceInterface {

    /**
     * Отправляет приветственное письмо новому пользователю.
     * Обычно содержит информацию о регистрации и приветствие.
     *
     * @param email адрес электронной почты получателя
     * @param name  имя пользователя для персонализации письма
     * @implNote Письмо должно быть персонализированным и содержать welcoming-контент
     */
    void sendWelcomeEmail(String email, String name);

    /**
     * Отправляет уведомление об удалении аккаунта пользователя.
     * Обычно содержит подтверждение удаления и прощальное сообщение.
     *
     * @param email адрес электронной почты получателя
     * @param name  имя пользователя для персонализации письма
     * @implNote Письмо должно подтверждать факт удаления аккаунта и содержать соответствующую информацию
     */
    void sendAccountDeletionEmail(String email, String name);

    /**
     * Отправляет произвольное электронное письмо с указанной темой и содержимым.
     * Универсальный метод для отправки любых типов email-сообщений.
     *
     * @param to      адрес электронной почты получателя
     * @param subject тема письма
     * @param message содержимое письма (может быть как plain text, так и HTML)
     */
    void sendEmail(String to, String subject, String message);
}