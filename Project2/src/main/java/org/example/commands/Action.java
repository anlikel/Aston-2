package org.example.commands;

/**
 * Доступные действия в системе.
 * Перечисление представляет собой набор операций CRUD (Create, Read, Update, Delete)
 * и дополнительных системных команд.
 */
public enum Action {

    /**
     * Завершение работы приложения.
     * Используется для корректного выхода из системы.
     */
    EXIT,

    /**
     * Создание новой сущности.
     * Операция добавления новых данных в систему.
     */
    CREATE,

    /**
     * Чтение информации о конкретной сущности.
     * Операция получения детальной информации по идентификатору.
     */
    READ,

    /**
     * Обновление существующей сущности.
     * Операция изменения данных уже существующей записи.
     */
    UPDATE,

    /**
     * Удаление сущности из системы.
     * Операция безвозвратного удаления данных.
     */
    DELETE,

    /**
     * Получение списка всех сущностей.
     * Операция чтения всех доступных записей без фильтрации.
     */
    READ_ALL
}